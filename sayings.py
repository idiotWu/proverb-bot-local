def pick_saying(emotion: str) -> Tuple[str, str]:
    """
    指定の感情に対して名言をランダムに 1 つ選ぶ

    Args:
        emotion: 感情

    Returns: (発話者, 名言)
    """
    proverbs: List[Tuple[str, str]] = sayings[emotion]['proverbs']
    return random.choice(proverbs)
